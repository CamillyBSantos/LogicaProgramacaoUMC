// Operadores Atitiméticos

let n1 = 9
let n2 = 2

// Soma
let soma= n1+n2
console.log("O resultado da soma é:",soma)

// Subtracao
let subtracao= n1-n2
console.log("O resultado da subtração é:", subtracao)

//Multiplicacao
let multiplicacao= n1*n2
console.log("O resultado da multiplicação é:", multiplicacao)

//Divisao
let divisao= n1/n2
console.log("O resultado da divisão é:", divisao)

//Modulo
let modulo = n1%n2
console.log("O resto da divisão é:", modulo)

//Potência
let potencia = Math.pow(n1,n2)
console.log("O resultado da potência é:", potencia)

//Raiz Quadrada
let raiz_quadrada = Math.sqrt(n1)
console.log("A raiz quadrada de", n1 , "é:", raiz_quadrada)