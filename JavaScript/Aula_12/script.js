// Variáveis - VAR e LET

// Declarando VAR
var texto = "Hello World"
console.log(texto)

// Declarando LET
let nome = "Camilly"
console.log(nome)

//Atribuindo um novo valor - LET
nome = "Iago"
console.log ("Nome:",nome)

//Atribuindo um novo valor - VAR
texto = "Olá, Mundo"
console.log (texto)

//Constantes - const
const valorPi = 3.14
console.log("O valor de PI é igual a" , valorPi)